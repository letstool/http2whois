// ForDomain returns the WHOIS server for a given domain name.
// It strips subdomains and resolves by TLD, falling back to IANA.
// IANA itself will return a refer: for any valid TLD, so the fallback
// is always safe.
func ForDomain(domain string) Server {
	parts := strings.Split(strings.ToLower(domain), ".")
	// Try two-level TLD first (e.g. co.uk, com.au), then single TLD
	if len(parts) >= 3 {
		twoLevel := parts[len(parts)-2] + "." + parts[len(parts)-1]
		if s, ok := TLDServers[twoLevel]; ok {
			return s
		}
	}
	if len(parts) >= 2 {
		tld := parts[len(parts)-1]
		if s, ok := TLDServers[tld]; ok {
			return s
		}
	}
	return IANAServer
}
