// Package servers maps TLDs, IP ranges and query types to their WHOIS servers.
// Source: IANA root zone database via https://github.com/ocku/whois-servers
// Last updated: 2026-03 (1 000+ TLDs).
package servers

import "strings"

// Server holds the address and optional query format for a WHOIS server.
type Server struct {
	Host   string // hostname, e.g. "whois.verisign-grs.com"
	Port   int    // default 43
	Format string // query format template, %s replaced by query; default "%s\r\n"
}

func (s Server) QueryString(query string) string {
	if s.Format != "" {
		return strings.ReplaceAll(s.Format, "%s", query) + "\r\n"
	}
	return query + "\r\n"
}

func (s Server) Addr() string {
	port := s.Port
	if port == 0 {
		port = 43
	}
	return s.Host + ":" + itoa(port)
}

func itoa(n int) string {
	if n == 0 {
		return "0"
	}
	buf := make([]byte, 0, 10)
	for n > 0 {
		buf = append([]byte{byte('0' + n%10)}, buf...)
		n /= 10
	}
	return string(buf)
}

// plain constructs a plain server (no special query format).
func plain(host string) Server { return Server{Host: host} }

// fmted constructs a server with a custom query format (%s = query).
func fmted(host, format string) Server { return Server{Host: host, Format: format} }

